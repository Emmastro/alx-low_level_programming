#include <stdio.h>
/**
 * main - main block
 * Description: Get a random number and check its last digit, compare it with 5
 * Return: 0
 */
int main(void)
{
	for (char i = 0; i < 26; i++)
	{
        if (i + 97!='e' && i + 97!='q')
        {
            putchar(i + 97);
        }	
	}
	putchar('\n');
	return (0);
}
